$MESS ['SEC_OTP_DESCRIPTION_ABOUT'] = <<<'HTML'
Система авторизации с использованием одноразовых паролей (One-Time Password - OTP) разработана в рамках инициативы OATH.<br>
Реализация основана на алгоритме HMAC и хэш-функций SHA-1/SHA-256/SHA-512. На текущий момент поддерживается два алгоритма генерации:
<ul><li>по счетчику (HMAC-Based One-time Password, HOTP), описан в <a href="https://tools.ietf.org/html/rfc4226" target="_blank">RFC4226</a></li>
<li>по времени (Time-based One-time Password, TOTP), описан <a href="https://tools.ietf.org/html/rfc6238" target="_blank">RFC6238</a></li></ul>
Для расчета значения OTP принимаются два входных параметра - секретный ключ (начальное значение для генератора) и текущее значение счетчика (количество необходимых циклов генерации или текущее временя, в зависимости от выбранного алгоритма). Начальное значение хранится
как в самом устройстве, так и на сайте после инициализации устройства. В случае использования HOTP алгоритма, счетчик в устройстве увеличивается при каждой генерации OTP, на сервере - при каждой удачной аутентификации по OTP. В случае использования TOTP алгоритма, счетчик в устройстве не хранится, на сервере лишь корректируется возможное небольшое смещение времени устройства при каждой удачной аутентификации по OTP.<br>
Партия устройств OTP поставляется с зашифрованным файлом, содержащим начальные значения (секретные ключи) для всех устройств партии,
связанного с серийным номером устройства (печатается на корпусе устройства).<br>
В случае нарушения синхронизации счетчика генерации в устройстве и на сервере, её можно легко восстановить - привести значение на сервере в соответствие значению, хранящемуся в устройстве. Для этого администратор системы или сам пользователь (при наличии соответствующих разрешений) должен сгенерировать два последовательных значения одноразовых паролей (OTP) и ввести их в форму на сайте.<br>
